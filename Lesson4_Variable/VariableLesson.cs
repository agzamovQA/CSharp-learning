namespace VariableLesson
{
    class Variable
    {
        static void Main (string[] args)
        {
            int a = 3;

            Console.WriteLine(a);

            a = 4;
            
            Console.WriteLine(a);
            
            int e,b,c;
            
            e = 6;
            b = 7;
            c = 8;
            
            Console.WriteLine(e + b + c);
            
            bool variable = true;
            
            Console.WriteLine(variable);

            char z = '#';
            
            Console.WriteLine(z);
            
            string name = "Vova";
            
            Console.WriteLine(name);
        }
    }
}